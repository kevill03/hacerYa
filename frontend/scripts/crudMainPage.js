const getToken = () => localStorage.getItem("token");

// FUNCI√ìN DE FETCH GENERALIZADA (PARA GET, POST, PUT, DELETE)
const apiRequest = async (endpoint, method = "GET", body = null) => {
  const token = getToken();
  // Validar token para m√©todos que no sean GET (excepto login/register que no pasan por aqu√≠)
  if (!token && method !== "GET") {
    console.error("Token no encontrado para solicitud autenticada.");
    // Considerar redirigir a login o mostrar mensaje
    // window.location.href = "login.html";
    throw new Error("Autenticaci√≥n requerida.");
  }

  const options = {
    method: method,
    headers: {}, // Inicializar headers
  };

  if (token) {
    options.headers["Authorization"] = `Bearer ${token}`;
  }

  if (body) {
    options.headers["Content-Type"] = "application/json";
    options.body = JSON.stringify(body);
  }

  try {
    const response = await fetch(`${BASE_API_URL}${endpoint}`, options);

    // Manejo especial para respuestas sin contenido (ej. DELETE 204)
    if (response.status === 204) {
      return { success: true }; // Indica √©xito sin datos
    }

    // Intentar parsear JSON solo si hay contenido
    const contentType = response.headers.get("content-type");
    let data;
    if (contentType && contentType.indexOf("application/json") !== -1) {
      data = await response.json();
    } else {
      // Si no es JSON, verificar si la respuesta fue OK igualmente (podr√≠a ser texto)
      if (!response.ok) {
        // Si no fue OK y no es JSON, lanzar error gen√©rico
        throw new Error(
          `Error ${response.status}: Respuesta inesperada del servidor.`
        );
      }
      // Si fue OK pero no JSON (raro para una API REST), devolver un estado simple
      return { success: true, status: response.status };
    }

    if (!response.ok) {
      // Usar el mensaje del JSON si existe, sino un mensaje gen√©rico
      throw new Error(
        data.message ||
          data.error ||
          `Error ${response.status}: Fallo en la API.`
      );
    }
    return data; // Devuelve los datos JSON para GET, POST, PUT
  } catch (error) {
    console.error(`Error en API ${method} ${endpoint}:`, error); // Loguear el error completo
    // Mostrar error al usuario de forma m√°s amigable
    // Por ejemplo, podr√≠as tener un div#error-message en tu HTML
    // document.getElementById('error-message').textContent = `Error: ${error.message}`;
    throw error; // Propagar el error para manejo espec√≠fico si es necesario
  }
};

